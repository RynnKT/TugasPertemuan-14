    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (jPasswordField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Password harap diisi");
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan_14PU");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();

        String user = tfUser.getText();
        String pw = jPasswordField1.getText();

        Akun x = em.find(Akun.class, user);

        x.setPassword(pw);
        em.getTransaction().commit();

        JOptionPane.showMessageDialog(null, "Password berhasil diubah");
        em.close();
        emf.close();
        this.dispose();
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        String user = tfUser.getText();
        String kode = tfkodeunik.getText();

        if (user.equals("") || kode.equals("")) {
            JOptionPane.showMessageDialog(null, "Isi username dan kode unik dulu!");
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan_14PU");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();

        Akun y = em.find(Akun.class, user);

        if (y == null) {
            JOptionPane.showMessageDialog(null, "Akun tidak ditemukan");
            bersih();
            tfUser.requestFocus();
        } else if (!kode.equals(y.getKodeUnik())) {
            JOptionPane.showMessageDialog(null, "Nama Hewan salah!");
            tfkodeunik.requestFocus();
        } else {
            // Username + kode unik valid â†’ tampilkan form ubah password
            lblPass.setVisible(true);
            btnUbah.setVisible(true);
            jPasswordField1.setVisible(true);

            tfUser.setEditable(false);
            tfkodeunik.setEditable(false);
            jPasswordField1.requestFocus();
        }

        em.getTransaction().commit();
        em.close();
        emf.close();
    }//GEN-LAST:event_btnCariActionPerformed

